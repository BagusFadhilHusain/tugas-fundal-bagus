public class KelilingSegitiga {
	public static void main (String[] args) {
		
		//Diketahui
		int SisiSegitiga = 7;
		
		//Rumus
		int KelilingSegitiga = SisiSegitiga * 3;
		
		//Print
		System.out.println("Sisi Segitiga = " + SisiSegitiga);
		System.out.println("Rumusnya = 3 x " + SisiSegitiga);
		System.out.println("Keliling Segitiganya Adalah = " +KelilingSegitiga);
	}
}